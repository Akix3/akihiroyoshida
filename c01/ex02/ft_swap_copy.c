#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int x;
	int y;

	x = '2';
	y = '4';
	ft_swap(&x, &y);
	write(1, &x, 1);
	write(1, &y, 1);
	return (0);
}
