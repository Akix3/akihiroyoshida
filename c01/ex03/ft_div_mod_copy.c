#include <unistd.h>
#include <stdio.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int	main(void)
{
	int c;
	int d;

	ft_div_mod(2142, 50, &c, &d);
	printf("%d-%d\n", c, d);
	return (0);
}
