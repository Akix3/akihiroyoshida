int	ft_is_prime(int nb)
{
	int n;

	if (nb < 0 || nb == 0 || nb == 1)
		return (0);
	n = (nb - 1);
	while (n > 1)
	{
		if (nb % n == 0)
			return (0);
		n--;
	}
	return (1);
}
